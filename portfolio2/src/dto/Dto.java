package dto;

public class Dto {

	/*
	 * 名前
	 * */
	private String name;
	
	/*
	 * パスワード
	 * */
	private String pass;
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPass() {
		return pass;
	}
	

	public void setPass(String pass) {
		this.pass = pass;
	}
}
